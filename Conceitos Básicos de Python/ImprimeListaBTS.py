# Declarando a lista bts = ['V', 'J-Hope', 'RM', 'Jungkook', 'Jin', 'Jimin', 'Suga']. 
# Em seguida, escreva um código que imprima todos os itens desta lista usando a declaração for.
# Exemplo de saída:
# 
# V
# 
# J-Hope
# 
# RM
# 
# Jungkook
# 
# Jin
# 
# Jimin
# 
# Suga


bts = ['V', 'J-Hope', 'RM', 'Jungkook', 'Jin', 'Jimin', 'Suga']

# Usando um loop for para imprimir cada item da lista
